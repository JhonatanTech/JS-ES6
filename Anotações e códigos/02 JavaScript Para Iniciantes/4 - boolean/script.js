
//operadores lógicos &&
//o operador && retorna sempre o primeiro valor false ou o ultimo caso seja ambos verdadeiros, como 'gato' && 'cão' return = cão
true && true //true
true && false //false
false && true //false
'gato' && 'cão' //cão
(5 - 5) && (5 + 5) //0
'gato' && false //false
(5 >= 5) && (3 < 6) //true

//o operador || retorna sempre o primeiro valor true
true || true //true
true || false //true
false || true //true
'gato' || 'cão' //gato
(5 - 5) || (5 + 5) //10
'gato' || false //gato
(5 >= 5) || (3 < 6) //true